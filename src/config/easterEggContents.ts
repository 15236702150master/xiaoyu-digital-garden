// 获取彩蛋内容（根据角色模式）
export function getEasterEggContent(eggId: string, mode: EasterEggMode): { title: string; content: string; icon: string } | null {
  const eggConfig = easterEggContents[eggId]
  if (!eggConfig) return null
  
  // 如果是随机模式，随机选择一个角色
  if (mode === 'random') {
    const modes: EasterEggMode[] = ['sunyou', 'father', 'nagging', 'dushe', 'jitang', 'shadian']
    mode = modes[Math.floor(Math.random() * modes.length)]
  }
  
  return eggConfig[mode] || eggConfig.sunyou
}
